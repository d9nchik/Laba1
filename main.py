k = int(input('Введіть k-у секунду: '))  # k - к-ть секунд з початку доби
k_real = k % 60  # k_real - кількість секунд при розкладі на секунди хвилини і годиниє
k -= k_real
m = (k / 60) #m - к-ть хвилин + к-ть годин у хвилинах
m_real = (m % 60)  #m_real - к-ть хвилин з початку доби
m -= m_real
h = m / 60 # к-ть годин з початку доби
print("Пройшло %s повних годин, %s повних хвилин з початка доби до k-ої секунди."
      % (int(h), int(m_real)))
